package task1;
/*simple calculator program for addition,multiplication and division of two numbers*/


public class Calculator {
 public int addition(int a ,int b) {
	 return a+b;
 }
 public int multiplication(int a,int b)
 {
	 return a*b;
 }
 public double division(int a ,int b) {
	 double c=(double)a/b;
	 return c;
 }
}
